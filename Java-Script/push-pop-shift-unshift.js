// array is mutable
//so

let fruits =["Apple", "mango","Grapes"];
console.log(fruits);

//push
fruits.push("banana"); // add last in array
console.log(fruits);


//pop
fruits.pop();
console.log(fruits); // delete  last val from array


//unshift
fruits.unshift("banana");  // add in first of the array
console.log(fruits);


// shift
fruits.shift();
console.log(fruits); // delete first val from array

